func extract_instructions(opcode: Int) -> (Int, Int, Int, Int) {
    let instruction = opcode % 100
    let param0 = (opcode / 100) % 10
    let param1 = (opcode / 1000) % 10
    let param2 = (opcode / 10000) % 10
    return (instruction, param0, param1, param2)
}

func extract_params(input: [Int], param_mode: Int, index: Int) -> Int {
    if param_mode == POSITION_MODE {
        return input[input[index]]
    } else {
        return input[index]
    }
}

let INPUT_PART1 = 1
let INPUT_PART2 = 5

func day05First() {
    var input = get_input()
    var index = 0
    while index < input.count {
        print("index", index)
        if input[index] == 99 {
            break
        }

        let opcode = input[index];
        let (instruction, param0, param1, param2) = extract_instructions(opcode: opcode)
        // print(
        //     "opcode {}, instruction {}, p0 {}, p1 {}, p2 {}",
        //     opcode, instruction, param0, param1, param2,
        // );
        assert(param2 == 0)
        var instruction_move = 4
        var jump = -1
        if instruction == 1 {
            // add
            let lhs = extract_params(input:input, param_mode:param0, index:index + 1)
            let rhs = extract_params(input:input, param_mode:param1, index:index + 2)
            let target = input[index + 3]
            input[target] = lhs + rhs
        } else if instruction == 2 {
            // multiply
            let lhs = extract_params(input:input, param_mode:param0, index:index + 1)
            let rhs = extract_params(input:input, param_mode:param1, index:index + 2)
            let target = input[index + 3]
            input[target] = lhs * rhs
        } else if instruction == 3 {
            // input
            let target = input[index + 1]
            let input_value = INPUT_PART2
            input[target] = input_value
            instruction_move = 2
            print("Wrote \(input_value) to address \(target)");
        } else if instruction == 4 {
            // output
            let target = input[index + 1]
            print("### Output is", input[target])
            instruction_move = 2
        } else if instruction == 5 {
            let condition = extract_params(input:input, param_mode:param0, index:index + 1) != 0
            let address = extract_params(input:input, param_mode:param1, index:index + 2)
            print("Jump {} to {}", condition, address)
            if condition {
                jump = address
            } else {
                instruction_move = 3
            }
        } else if instruction == 6 {
            let condition = extract_params(input:input, param_mode:param0, index:index + 1) == 0
            let address = extract_params(input:input, param_mode:param1, index:index + 2)
            print("Jump {} to {}", condition, address)
            if condition {
                jump = address
            } else {
                instruction_move = 3
            }
        } else if instruction == 7 {
            let lhs = extract_params(input:input, param_mode:param0, index:index + 1)
            let rhs = extract_params(input:input, param_mode:param1, index:index + 2)
            let target = input[index + 3]
            if lhs < rhs {
                input[target] = 1
            } else {
                input[target] = 0
            }
        } else if instruction == 8 {
            let lhs = extract_params(input:input, param_mode:param0, index:index + 1)
            let rhs = extract_params(input:input, param_mode:param1, index:index + 2)
            let target = input[index + 3]
            if lhs == rhs {
                input[target] = 1
            } else {
                input[target] = 0
            }
        }

        if jump >= 0 {
            index = jump
        } else {
            index += instruction_move
        }
    }
    // print("position 0: {}", input[0])
}
